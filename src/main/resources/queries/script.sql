CREATE TABLE CLIENTES(
    IdCliente NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NombreCliente VARCHAR(10) NOT NULL,
    Password VARCHAR(60) NOT NULL,
    FechaCreacion VARCHAR(100) NOT NULL,
    Estado VARCHAR2(3) NOT NULL,
    PRIMARY KEY(idCliente)
);


CREATE TABLE PRODUCTOS(
    IdProducto NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NombreProducto VARCHAR2(100) NOT NULL,
    FechaCreacion VARCHAR2(500) NOT NULL,
    Estado VARCHAR2(3) NOT NULL,
    PRIMARY KEY(IdProducto)
);

CREATE TABLE CLIENTES_PRODUCTOS(
    IdClientesProductos NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    IdClientes NUMBER(10) NOT NULL,
    IdProductos NUMBER(10) NOT NULL,
    PRIMARY KEY(IdClientesProductos),
    CONSTRAINT fk_clientes_productos FOREIGN KEY(IdClientes) REFERENCES CLIENTES(IdClientes),
    CONSTRAINT fk_productos_clientes FOREIGN KEY(IdProductos) REFERENCES PRODUCTOS(IdProductos)
);
