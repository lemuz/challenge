CREATE TABLE CLIENTES(
    IdCliente NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NombreCliente VARCHAR(10) NOT NULL,
    FechaCreacion DATE NOT NULL,
    Estado NUMBER(1,0) NOT NULL,
    PRIMARY KEY(idCliente)
);

CREATE TABLE PRODUCTOS(
    IdProducto NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NombreProducto VARCHAR2(100) NOT NULL,
    FechaCreacion DATE NOT NULL,
    Estado NUMBER(1,0) NOT NULL,
    PRIMARY KEY(IdProducto)
);

CREATE TABLE PRODUCTOS_CLIENTES(
    IdProductosClientes NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    IdClientes NUMBER(10) NOT NULL,
    IdProductos NUMBER(10) NOT NULL,
    PRIMARY KEY(IdProductosClientes),
    CONSTRAINT fk_clientes_productos FOREIGN KEY(IdClientes) REFERENCES CLIENTES(IdCliente),
    CONSTRAINT fk_productos_clientes FOREIGN KEY(IdProductos) REFERENCES PRODUCTOS(IdProducto)
);
